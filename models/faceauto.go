		contentstr += "表情:" + get_faceadd_expression(result.Attributes.Emotion.Anger, result.Attributes.Emotion.Disgust, result.Attributes.Emotion.Fear, result.Attributes.Emotion.Happiness, result.Attributes.Emotion.Neutral, result.Attributes.Emotion.Sadness, result.Attributes.Emotion.Surprise) + "\n"
